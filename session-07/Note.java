public class Note {
	// Fields
	private String filePath ;
	private String context ;
	// Constructor
	public Note(String filePath , String context) {
		this.filePath = filePath ;
		this.context = context ;
	}
	// Methods
	public String getFilePath() {
		return filePath ;
	}
	public String getContext() {
		return context ;
	}
}

