public class Note {
	// Fields
	private String filename ;
	private String context ;
	// Constructor
	public Note(String filename , String context) {
		this.filename = filename ;
		this.context = context ;
	}
	// Methods
	public String getFilename() {
		return filename ;
	}
	public String getContext() {
		return context ;
	}
}

